    private void TiempoAtrasos() {
        try{
            this.tiempoAtrasos = this.tiempoAtrasos_servicio.recuperarTodas(Conexion.obtener());
            DefaultTableModel dtm = (DefaultTableModel) jTableTroqueles.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.tiempoAtrasos.size(); i++){
                dtm.addRow(new Object[]{
                    this.tiempoAtrasos.get(i).getOrden(),
                    this.tiempoAtrasos.get(i).getCantidad_reque(),
                    this.tiempoAtrasos.get(i).getComponente(),
                    this.tiempoAtrasos.get(i).getNumOperaciones(),
                    this.tiempoAtrasos.get(i).getTroquel(),
                    this.tiempoAtrasos.get(i).getMaquina(),
                    this.tiempoAtrasos.get(i).getInst_desm(),
                    this.tiempoAtrasos.get(i).getTiempo_troquelado(),
                    this.tiempoAtrasos.get(i).getTiempo_total()
                });
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
