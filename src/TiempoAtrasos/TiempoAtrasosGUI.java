            //sumar horas por maquina
            PreparedStatement pst2 = cn.prepareStatement("UPDATE tiempomaquinas SET horas=(SELECT SEC_TO_TIME(CAST(SUM(TIME_TO_SEC(tiempoatrasos.tiempo_total)) AS SIGNED)) FROM tiempoatrasos,troqueles WHERE tiempoatrasos.componente= troqueles.componente AND tiempoatrasos.maquina=tiempomaquinas.maquina AND troqueles.troquel= tiempoatrasos.troquel AND tiempoatrasos.orden=tiempoatrasos.orden)");
