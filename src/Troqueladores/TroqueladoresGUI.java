    private void troqueles() {

        try{
            this.troqueladores = this.troqueladores_servicio.recuperarTodas(Conexion.obtener());
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.troqueladores.size(); i++){
                dtm.addRow(new Object[]{
                    this.troqueladores.get(i).getId_troquelador(),
                    this.troqueladores.get(i).getNombre(),
                    this.troqueladores.get(i).getPuesto()                   
                });
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
    
