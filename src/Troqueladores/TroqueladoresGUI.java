    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try{
            int fila_seleccionada = jTable1.getSelectedRow();
            if(fila_seleccionada != -1){
                int ID = (int) jTable1.getValueAt(fila_seleccionada, 0);
                
                this.troqueladores_servicio.eliminar(ID);

                TroqueladoresGUI.this.dispose();
                JOptionPane.showMessageDialog(this, "DATOS ELIMINADOS");
                
                TroqueladoresGUI entregados = new TroqueladoresGUI(mod);
                entregados.setVisible(true);
                entregados.setLocationRelativeTo(null);
                
            }else{
                JOptionPane.showMessageDialog(this, "Por favor seleccione una fila.");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jTable1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTable1KeyReleased
         if(evt.getKeyCode() == com.sun.glass.events.KeyEvent.VK_ENTER){
            try {
                String id_troquelador = jTable1.getValueAt(jTable1.getSelectedRow(),0).toString();
                String nombre = jTable1.getValueAt(jTable1.getSelectedRow(),1).toString();
                String puesto = jTable1.getValueAt(jTable1.getSelectedRow(),2).toString();
                
                PreparedStatement consulta;
                consulta = cn.prepareStatement("UPDATE troqueladores SET nombre='"+nombre+"',puesto='"+puesto+"' WHERE id_troquelador='"+id_troquelador+"'");
                consulta.executeUpdate();
                JOptionPane.showMessageDialog(this, "MODIFICACIÃ“N EXITOSA");
            } catch (SQLException ex) {
                Logger.getLogger(TroqueladoresGUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTable1KeyReleased

