    private void Ventas(){
        try{
            String anio=JOptionPane.showInputDialog("INGRESAR AÃ‘O:");
             
            PreparedStatement consulta;
            consulta = cn.prepareStatement("SELECT componente,cr, SUM(pzs) as Total FROM ventas WHERE SUBSTRING(fechaEmb, 7, 4)='"+anio+"' GROUP BY componente ORDER BY Total DESC, componente ASC");
            ResultSet resultado = consulta.executeQuery();
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while(resultado.next()){
               dtm.addRow(new Object[]{
                   resultado.getString("componente"),
                   resultado.getString("cr"), 
                   resultado.getString("Total"), 
               });
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
    
