    private void Ventas(){
        try{
            this.ventas = this.ventas_servicio.recuperarTodas(Conexion.obtener());
            
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.ventas.size(); i++){
                dtm.addRow(new Object[]{
                    this.ventas.get(i).getId(),
                    this.ventas.get(i).getComponente(),
                    this.ventas.get(i).getCr(),
                    this.ventas.get(i).getPzs(),
                    this.ventas.get(i).getFechaEmb()
                });
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
    
