    public void suma(){
        try {
            jLabel7.setText("");
            Statement comando=cn.createStatement();
            //TOTAL DE PIEZAS
            ResultSet registroA = comando.executeQuery("SELECT SUM(pzs) AS total FROM ventas");
            if(registroA.next()==true) {
                jLabel7.setText(registroA.getString("total"));                
            }
        } catch (SQLException ex) {
            Logger.getLogger(VentasGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
