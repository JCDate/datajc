    private void ProveedoresGUI() {
        try{
            this.prov = this.proveedores_servicio.recuperarTodas(Conexion.obtener());
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.prov.size(); i++){
                dtm.addRow(new Object[]{
                    this.prov.get(i).getNombre(),
                    this.prov.get(i).getColonia(),
                    this.prov.get(i).getParque(),
                    this.prov.get(i).getCodigoPostal(),
                    this.prov.get(i).getCiudad(),
                    this.prov.get(i).getEstado(),
                    this.prov.get(i).getPais(),
                    this.prov.get(i).getCalibre(),
                    
                    
                });
                
            }
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }  
}
    
