    private void jTableTroquelesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableTroquelesKeyReleased
        if(evt.getKeyCode() == com.sun.glass.events.KeyEvent.VK_ENTER){
            try {
                String componente = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),0).toString();
                String numOperaciones = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),1).toString();
                String caja = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),2).toString();
                String troquel = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),3).toString();
                String opciones = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),4).toString();
                String maquinaOp = jTableTroqueles.getValueAt(jTableTroqueles.getSelectedRow(),5).toString();
                
                PreparedStatement consulta;
                consulta = cn.prepareStatement("UPDATE troqueles SET caja='"+caja+"',troquel='"+troquel+"',opciones='"+opciones+"',maquinaOp='"+maquinaOp+"' WHERE componente='"+componente+"' AND numOperaciones='"+numOperaciones+"'");
                consulta.executeUpdate();
                JOptionPane.showMessageDialog(this, "MODIFICACIÓN EXITOSA");
            } catch (SQLException ex) {
                Logger.getLogger(troqueles.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTableTroquelesKeyReleased

