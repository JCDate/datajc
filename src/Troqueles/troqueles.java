    private void troqueles() {

        try{
            this.troqueles = this.troqueles_servicio.recuperarTodas(Conexion.obtener());
            DefaultTableModel dtm = (DefaultTableModel) jTableTroqueles.getModel();
            dtm.setRowCount(0);
            for(int i = 0; i < this.troqueles.size(); i++){
                dtm.addRow(new Object[]{
                    this.troqueles.get(i).getComponente(),
                    this.troqueles.get(i).getNumOperaciones(),
                    this.troqueles.get(i).getCaja(),
                    this.troqueles.get(i).getTroquel(),
                    this.troqueles.get(i).getOpciones(),
                    this.troqueles.get(i).getMaquinaOp()                    
                });
            }
            //componente y numero de operacion
           /* PreparedStatement pst = cn.prepareStatement("INSERT INTO tiemposyrecursos (tiemposyrecursos.id_tiemposRecurso, tiemposyrecursos.componente,tiemposyrecursos.numeroOper) SELECT troqueles.id_troquel, troqueles.componente, troqueles.numOperaciones FROM troqueles WHERE NOT EXISTS ( SELECT tiemposyrecursos.id_tiemposRecurso FROM tiemposyrecursos WHERE tiemposyrecursos.id_tiemposRecurso = troqueles.id_troquel)");
            pst.executeUpdate();*/
            
        }catch(SQLException ex){
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }catch(ClassNotFoundException ex){
            System.out.println(ex);
            JOptionPane.showMessageDialog(this, "Ha surgido un error y no se han podido recuperar los registros");
        }
    }
    
